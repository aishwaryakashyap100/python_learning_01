# Adding two numbers
sum_result = 10 + 5        # Adds 10 and 5 ➔ 15

# Subtracting
difference = 10 - 5        # Subtracts 5 from 10 ➔ 5

# Multiplication
product = 10 * 5           # Multiplies 10 and 5 ➔ 50

# Division (gives float)
division = 10 / 2          # Divides 10 by 2 ➔ 5.0

# Floor Division (whole number result)
floor_div = 10 // 3        # Divides 10 by 3 ➔ 3

# Modulus (remainder)
remainder = 10 % 3         # Gives remainder ➔ 1

# Exponentiation (power)
power = 2 ** 3             # 2 to the power 3 ➔ 8