# Looping over a range of numbers
for i in range(5):                      # Runs 5 times (i = 0,1,2,3,4)
    print("Iteration:", i)               # Prints each iteration
    
# A list of fruits
fruits = ["apple", "banana", "mango"]

# Looping through each fruit
for fruit in fruits:
    print("Fruit:", fruit)
